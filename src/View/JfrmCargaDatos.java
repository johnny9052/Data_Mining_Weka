    private void JbtnSeleccionarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnSeleccionarArchivoActionPerformed
        //Ruta del archivo       
        String ruta;
        //se crea el FileChosser 
        final JFileChooser chooser = new JFileChooser();
        //Se definen los formatos soportados
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos arff", "arff");
        //Se le añaden los formatos al fileChooser
        chooser.setFileFilter(filter);
        //Se abre el file chooser, asignandolo como hijo al jpanel principal
        chooser.showOpenDialog(JpIndex);
        //Se captura la ruta del archivo seleccionado
        ruta = chooser.getSelectedFile().toString();
        //Se muestra la ruta en pantalla
        JlblRuta.setText(ruta);

        try {
            if (!ruta.equals("")) { //si se ha seleccionado archivo
                //Se lee el archivo
                BufferedReader file = new BufferedReader(new FileReader(ruta));
                //se le añade el contenido al objeto de tipo mensaje, convirtiendo el Buffer en un String
                JtxtAFile.setText(convertir(file));
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_JbtnSeleccionarArchivoActionPerformed

