    public String convertir(BufferedReader file) throws IOException {
        //Almacena la linea leida del file
        String temp;
        //cadena final
        String cadena = "";
        //Mienstras pueda leer una nueva linea del buffer
        while ((temp = file.readLine()) != null) {
            //Concatenelo
            cadena = cadena + temp + "\n";
        }
        return cadena;
    }

